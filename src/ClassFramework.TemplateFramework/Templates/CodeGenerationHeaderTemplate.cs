namespace ClassFramework.TemplateFramework.Templates;

public sealed class CodeGenerationHeaderTemplate : CsharpClassGeneratorBase<CodeGenerationHeaderViewModel>, IStringBuilderTemplate
{
    public CodeGenerationHeaderTemplate(IViewModelFactory viewModelFactory) : base(viewModelFactory)
    {
    }

    public void Render(StringBuilder builder)
    {
        Guard.IsNotNull(builder);
        Guard.IsNotNull(Model);

        if (!Model.Settings.CreateCodeGenerationHeader)
        {
            return;
        }

        builder.AppendLine($$"""
// ------------------------------------------------------------------------------
// <auto-generated>
//     This code was generated by a tool.
//     Runtime Version: {{Model.Version}}
//  
//     Changes to this file may cause incorrect behavior and will be lost if
//     the code is regenerated.
// </auto-generated>
// ------------------------------------------------------------------------------
""");
    }
}
